#include <numbers>
#include <iostream>

////////////////////////////////////////////////////////////////////////////////////////////
// Circle class methods
////////////////////////////////////////////////////////////////////////////////////////////

void Circle::Size() {
	size_ = radius * radius * std::numbers::pi_v<float>;
}

void Circle::Draw() {
	std::cout << "circle size: " << size_ << std::endl;
}

////////////////////////////////////////////////////////////////////////////////////////////
// Rectangle class methods
////////////////////////////////////////////////////////////////////////////////////////////

void Rectangle::Size() {
	size_ = width * height;
}

void Rectangle::Draw() {
	std::cout << "rectangle size: " << size_ << std::endl;
}
