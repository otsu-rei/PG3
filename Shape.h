#pragma once

////////////////////////////////////////////////////////////////////////////////////////////
// Shape Interface class
////////////////////////////////////////////////////////////////////////////////////////////
class IShape {
public:

	//=========================================================================================
	// public methods
	//=========================================================================================
	
	virtual void Size() = 0;
	virtual void Draw() = 0;

protected:

	//=========================================================================================
	// public variables
	//=========================================================================================

	float size_;

};

////////////////////////////////////////////////////////////////////////////////////////////
// Circle class
////////////////////////////////////////////////////////////////////////////////////////////
class Circle
	: public IShape {
public:

	//=========================================================================================
	// public methods
	//=========================================================================================

	void Size() override;
	void Draw() override;

	//=========================================================================================
	// public variables
	//=========================================================================================

	float radius;

private:
};

////////////////////////////////////////////////////////////////////////////////////////////
// Rectangle class
////////////////////////////////////////////////////////////////////////////////////////////
class Rectangle
	: public IShape {
public:

	//=========================================================================================
	// public methods
	//=========================================================================================

	void Size() override;
	void Draw() override;

	//=========================================================================================
	// public variables
	//=========================================================================================

	float width;
	float height;

private:
};