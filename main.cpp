#include <cstdint>
#include <vector>

////////////////////////////////////////////////////////////////////////////////////////////
// methods
////////////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////////////////
// main
////////////////////////////////////////////////////////////////////////////////////////////
int main() {

	std::vector<int32_t> a;
	a.resize(4);

	for (int32_t i = 0; i < 4; ++i) {
		a[i] = i + 1;
	}

	std::vector<int32_t> b;
	b.resize(2);

	for (int32_t i = 0; i < 2; ++i) {
		b[i] = i * 4;
	}

	a = b;

	return 0;
}